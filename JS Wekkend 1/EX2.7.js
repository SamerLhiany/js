function basicmath (operation, num1, num2){
    let sum = 0 ;
    if (operation === `+`) {
        sum = num1 + num2
        return (sum)
    }
    if (operation === `-`) {
        sum = num1 - num2
        return (sum)
    }
    if (operation === `*`) {
        sum = num1 * num2
        return (sum)
    }
    if (operation === `/`) {
        sum = num1 / num2
        return (sum)
    }
    if (operation === `+`) {
        sum = num1 / num2
        return (sum)
    }
}

